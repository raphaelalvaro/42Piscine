#include <stdio.h>
#include <unistd.h>

int	ft_sqrt(int nb)
{
	int	i;

	i = 0;
	while (i * i <= nb)
	{	
		if (i * i == nb <= 46340)
			return (i);
		i++;
	}
	return (0);
}
